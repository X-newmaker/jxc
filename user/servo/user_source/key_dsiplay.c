const s16 keyChangeLevel[5] = {1,10,100,1000,10000};//按键在不同数码管位置的加减量纲单位
s32 pa_modify_disp(KEYVALUE key,PARAMETER_DATA par)
{
	int16_t k;
	PARAMETER_DATA *PARA_ptrAll = (void*) &Servo.Pn00.SoftVerion;
	if (ShitfKeyPushFlag == 0)
	{
		if (key == KEY_UP)  //up key
		{
			if (key_code_3s == 1)
			{
				par.Data += 4;
			}
			else if (key_code_2s == 1)
			{
				par.Data += 2;
			}
			else
			{
				par.Data++;
			}
		}
		else if (key == KEY_DOWN)         //down key
		{
			if (key_code_3s == 1)
			{
				par.Data -= 4;
			}
			else if (key_code_2s == 1)
			{
				par.Data -= 2;
			}
			else
			{
				par.Data--;
			}
		}
	}
	else if (ShitfKeyPushFlag == 1)
	{
		if (key == KEY_UP)         //up key
		{
			par.Data += keyChangeLevel[LedDispPosition];
		}
		else if (key == KEY_DOWN)
		{
			par.Data -= keyChangeLevel[LedDispPosition];
		}
	}
	k = get_Px_K(disp_mod);//获取k值
	if (par.Data > (PARA_ptrAll + k)->Max)
	{
		par.Data = (PARA_ptrAll + k)->Min;
	}
	else if (par.Data < (PARA_ptrAll + k)->Min)
	{
		par.Data = (PARA_ptrAll + k)->Max;
	}
	return par.Data;
}

