//====================================================================
//二进制数转换成字符串1,输入输出都是um单位,无小数点处理
//返回值是字符串长度
//asicii_out[0]是最高位(可能是符号位)
int16_t hex2ascii_1(long var1, int16_t ascii_out[12])
{
	int16_t i = 0;
	int16_t j = 0;
	int16_t m = 0;
	int16_t ml = 0;
	long subdata = 0;

	for (i = 0; i < 12; i++)
	{
		ascii_out[i] = 0x20;
	}
	if (var1 == 0)
	{
		ascii_out[0] = 0x30;
	}
	else if (var1 < 0)
	{
		ascii_out[0] = 0x2d;
		var1 = -var1;
		m = 1;
	}
	else
	{
		m = 0;
	}

	for (j = 0; j < 10; j++)  //10位
	{
		switch (j)
		{
			case 0:
				subdata = 1000000000;
			break;
			case 1:
				subdata = 100000000;
			break;
			case 2:
				subdata = 10000000;
			break;
			case 3:
				subdata = 1000000;
			break;
			case 4:
				subdata = 100000;
			break;
			case 5:
				subdata = 10000;
			break;
			case 6:
				subdata = 1000;
			break;
			case 7:
				subdata = 100;
			break;
			case 8:
				subdata = 10;
			break;
			case 9:
				subdata = 1;
			break;
			default:
			break;
		}

		if (ml == 0)
		{
			if (var1 >= subdata)
			{
				ml = 1;
			}
		}

		if (ml == 1)
		{
			for (i = 0; i < 10; i++)
			{
				var1 -= subdata;
				if (var1 < 0)
				{
					ascii_out[m] = i + 0x30;
					m++;
					var1 += subdata;
					break;
				}
			}
		}
	}
	for (i = 0; i < 12; i++)
	{
		if (ascii_out[i] == 0x20)
		{
			break;
		}
	}
	return i;
}

